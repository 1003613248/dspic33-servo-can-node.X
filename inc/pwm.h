////////////////////////////////////////////////////////////////////////////////

#ifndef PWM_H_
#define PWM_H_

// *****************************************************************************
// ************************** System Include Files *****************************
// *****************************************************************************

#include <xc.h>
#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

// *****************************************************************************
// ************************** User Include Files *******************************
// *****************************************************************************

// *****************************************************************************
// ************************** Defines ******************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Declarations *************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Function Prototypes ******************************
// *****************************************************************************

////////////////////////////////////////////////////////////////////////////////
/// @brief  Initialize PWM hardware.
////////////////////////////////////////////////////////////////////////////////
void PWMInit ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Enable PWM hardware for operation.
///
/// @note   Independent function for enabling of PWM operation allows for
///         synchronization with control-flow processing.
////////////////////////////////////////////////////////////////////////////////
void PWMEnable ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Set the PWM duty cycle.
///
/// @param  pwm_duty
///             Selected duty cycle (LSB = 1us).
////////////////////////////////////////////////////////////////////////////////
void PWMDutySet ( uint16_t pwm_duty );

#endif  // PWM_H_