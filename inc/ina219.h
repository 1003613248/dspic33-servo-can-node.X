////////////////////////////////////////////////////////////////////////////////
/// @file
/// @brief External current/power monitor (INA219) driver. 
////////////////////////////////////////////////////////////////////////////////

#ifndef INA219_H_
#define	INA219_H_

// *****************************************************************************
// ************************** System Include Files *****************************
// *****************************************************************************

#include <xc.h>
#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

// *****************************************************************************
// ************************** User Include Files *******************************
// *****************************************************************************

// *****************************************************************************
// ************************** Defines ******************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Declarations *************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Function Prototypes ******************************
// *****************************************************************************

////////////////////////////////////////////////////////////////////////////////
/// @brief  Initialize INA219 hardware.
////////////////////////////////////////////////////////////////////////////////
void INA219Init ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Service the INA219 module.
///
/// Read the INA219 measured current and voltage values into module data.
////////////////////////////////////////////////////////////////////////////////
void INA219Service ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Returns the measured current (i.e. amperage) value from module data.
///
/// @return Measured current value (LSB = 1mA).
////////////////////////////////////////////////////////////////////////////////
uint16_t INA219AmpGet ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Returns the measured voltage value from module data.
/// 
/// @return Measured voltage value (LSB = 1mV).
////////////////////////////////////////////////////////////////////////////////
uint16_t INA219VoltGet ( void );

#endif	// INA219_H_

