////////////////////////////////////////////////////////////////////////////////
/// @file
/// @brief Timer (TMR) driver.
////////////////////////////////////////////////////////////////////////////////

#ifndef TMR_H_
#define	TMR_H_

// *****************************************************************************
// ************************** System Include Files *****************************
// *****************************************************************************

#include <xc.h>
#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

// *****************************************************************************
// ************************** User Include Files *******************************
// *****************************************************************************

// *****************************************************************************
// ************************** Defines ******************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Declarations *************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Function Prototypes ******************************
// *****************************************************************************

////////////////////////////////////////////////////////////////////////////////
/// @brief  Initialize Timer hardware.
////////////////////////////////////////////////////////////////////////////////
void TMRInit ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Enable Timer1 operation.
////////////////////////////////////////////////////////////////////////////////
void TMR1Enable ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Disable Timer1 operation.
///
/// @note   The timer counter value is cleared when disabling operation.
////////////////////////////////////////////////////////////////////////////////
void TMR1Disable ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Service Timer1 - clear interrupt flag.
////////////////////////////////////////////////////////////////////////////////
void TMR1Service ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Service Timer2 - increment counter and clear interrupt flag.
////////////////////////////////////////////////////////////////////////////////
void TMR2Service ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Return Timer2 0.1ms counter.
///
/// @return The 0.1ms counter value.
////////////////////////////////////////////////////////////////////////////////
uint16_t TMR2p1msGet ( void );

#endif	// TMR_H_