////////////////////////////////////////////////////////////////////////////////
/// @file   
/// @brief  Inter-Integrated Circuit (I2C) driver.
////////////////////////////////////////////////////////////////////////////////

#ifndef I2C_H_
#define	I2C_H_

// *****************************************************************************
// ************************** System Include Files *****************************
// *****************************************************************************

#include <xc.h>
#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

// *****************************************************************************
// ************************** User Include Files *******************************
// *****************************************************************************

// *****************************************************************************
// ************************** Defines ******************************************
// *****************************************************************************

// *****************************************************************************
// ************************** User Include Files *******************************
// *****************************************************************************

// *****************************************************************************
// ************************** Declarations *************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Function Prototypes ******************************
// *****************************************************************************

////////////////////////////////////////////////////////////////////////////////
/// @brief  Initialize I2C hardware.
////////////////////////////////////////////////////////////////////////////////
void I2CInit( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Write data using the I2C interface.
///
/// @param  saddr
///             The slave address of the peripheral being accessed.
/// @param  data
///             The data to write to the peripheral.
/// @param  data_len
///             The number of bytes to write.
////////////////////////////////////////////////////////////////////////////////
void I2CWrite( uint8_t saddr, const uint8_t* data, uint8_t data_len );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Read data using the I2C interface.
///
/// @param  saddr
///             The slave address of the peripheral being accessed.
/// @param  data
///             The buffer to populate with read data.
/// @param  data_len
///             The number of bytes to read.
////////////////////////////////////////////////////////////////////////////////
void I2CRead( uint8_t saddr, uint8_t* data, uint8_t data_len );

#endif	// I2C_H_

