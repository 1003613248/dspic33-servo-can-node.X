////////////////////////////////////////////////////////////////////////////////
/// @file
/// @brief Analog to Digital Converter (ADC) driver.
////////////////////////////////////////////////////////////////////////////////

#ifndef ADC_H_
#define	ADC_H_

// *****************************************************************************
// ************************** System Include Files *****************************
// *****************************************************************************

#include <xc.h>
#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

// *****************************************************************************
// ************************** User Include Files *******************************
// *****************************************************************************

// *****************************************************************************
// ************************** Defines ******************************************
// *****************************************************************************

/// ADC input signals.
typedef enum
{
    ADC_VSENSE1,
    ADC_VSENSE2,
    
    ADC_AIN_MAX
    
} ADC_AIN_E;

// *****************************************************************************
// ************************** Declarations *************************************
// *****************************************************************************

// *****************************************************************************
// ************************** Function Prototypes ******************************
// *****************************************************************************

////////////////////////////////////////////////////////////////////////////////
/// @brief  Initialize ADC hardware.
////////////////////////////////////////////////////////////////////////////////
void ADCInit ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Refresh module data with ADC values.
////////////////////////////////////////////////////////////////////////////////
void ADCService ( void );

////////////////////////////////////////////////////////////////////////////////
/// @brief  Return ADC signal value from module data.
///
/// @param  adc_sel
///             The selected ADC signal value to be returned.
///
/// @return The ADC value.
///
/// @note   Returned value size (i.e. 10 or 12-bit) depends on ADxCON1.AD12B
///         register setting in function ADCInit.
////////////////////////////////////////////////////////////////////////////////
uint16_t ADCGet ( ADC_AIN_E adc_sel );

#endif	// ADC_H_